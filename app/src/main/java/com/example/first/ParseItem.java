package com.example.first;

public class ParseItem {

    private String day;
    private String eat1;
    private String eat2;
    private String eat3;
    private String eat4;
    private String eat5;

    public ParseItem() {
    }

    public ParseItem(String day, String eat1, String eat2, String eat3, String eat4, String eat5) {
        this.day = day;
        this.eat1 = eat1;
        this.eat2 = eat2;
        this.eat3 = eat3;
        this.eat4 = eat4;
        this.eat5 = eat5;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getEat1() {
        return eat1;
    }

    public void setEat1(String eat1) {
        this.eat1 = eat1;
    }

    public String getEat2() {
        return eat2;
    }

    public void setEat2(String eat2) {
        this.eat2 = eat2;
    }

    public String getEat3() {
        return eat3;
    }

    public void setEat3(String eat3) {
        this.eat3 = eat3;
    }

    public String getEat4() {
        return eat4;
    }

    public void setEat4(String eat4) {
        this.eat4 = eat4;
    }

    public String getEat5() {
        return eat5;
    }

    public void setEat5(String eat5) {
        this.eat5 = eat5;
    }
}
